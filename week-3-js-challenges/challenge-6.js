function findBiggerNumber(firstNum, secondNum) {
    if(firstNum > secondNum){
      return firstNum;
    }
    else{
      return secondNum;
    }   
  }
  
  console.log(findBiggerNumber(4,6)) // 6
  console.log(findBiggerNumber(87, 5)) // 87



